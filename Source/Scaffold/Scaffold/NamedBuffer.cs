using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Scaffold
{
	//*-------------------------------------------------------------------------*
	//*	NamedBufferCollection																										*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// Collection of NamedBufferItem Items.
	/// </summary>
	/// <remarks>
	/// Values in this collection are stored as a series of matched pairs in
	/// the form  {NameLength:uint}{Name}{DataLength:uint}{Data}.
	/// Any individual data field is limited to 4GB.
	/// </remarks>
	public class NamedBufferCollection : List<NamedBufferItem>
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************


	}
	//*-------------------------------------------------------------------------*

	//*-------------------------------------------------------------------------*
	//*	NamedBufferItem																													*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// Byte buffer with name.
	/// </summary>
	public class NamedBufferItem
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************
		//*-----------------------------------------------------------------------*
		//*	Data																																	*
		//*-----------------------------------------------------------------------*
		private byte[] mData = new byte[0];
		/// <summary>
		/// Get/Set the binary data portion of the buffer.
		/// </summary>
		public byte[] Data
		{
			get { return mData; }
			set { mData = value; }
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	Name																																	*
		//*-----------------------------------------------------------------------*
		private string mName = "";
		/// <summary>
		/// Get/Set the name of the buffer.
		/// </summary>
		public string Name
		{
			get { return mName; }
			set { mName = value; }
		}
		//*-----------------------------------------------------------------------*

	}
	//*-------------------------------------------------------------------------*
}
