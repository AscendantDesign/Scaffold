	//*-------------------------------------------------------------------------*

	//*-------------------------------------------------------------------------*
	//*	SlackUserItem																														*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// Individual Slack user information.
	/// </summary>
	public class SlackUserItem
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************
		//*-----------------------------------------------------------------------*
		//*	ID																																		*
		//*-----------------------------------------------------------------------*
		private string mID = "";
		/// <summary>
		/// Get/Set the Slack ID of the user.
		/// </summary>
		[JsonProperty(PropertyName = "id")]
		public string ID
		{
			get { return mID; }
			set { mID = value; }
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	Name																																	*
		//*-----------------------------------------------------------------------*
		private string mName = "";
		/// <summary>
		/// Get/Set the user's Slack first name.
		/// </summary>
		[JsonProperty("name")]
		public string Name
		{
			get { return mName; }
			set
			{
				mName = value;
				if(value?.Length > 0)
				{
					if(value.Length > 1)
					{
						mName = mName.Substring(0, 1).ToUpper() + mName.Substring(1);
					}
					else
					{
						mName = mName.ToUpper();
					}
				}
				else if(value == null)
				{
					//	Don't allow null.
					mName = "";
				}
			}
		}
		//*-----------------------------------------------------------------------*


	}
	//*-------------------------------------------------------------------------*

	//*-------------------------------------------------------------------------*
	//*	SlackUserList																														*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// Information returned from the Slack users.list method.
	/// </summary>
	public class SlackUserList
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************
		//*-----------------------------------------------------------------------*
		//*	Members																																*
		//*-----------------------------------------------------------------------*
		private SlackUserCollection mMembers = new SlackUserCollection();
		/// <summary>
		/// Get a reference to the collection of users in the list.
		/// </summary>
		[JsonProperty(PropertyName = "members")]
		public SlackUserCollection Members
		{
			get { return mMembers; }
		}
		//*-----------------------------------------------------------------------*


	}
	//*-------------------------------------------------------------------------*


